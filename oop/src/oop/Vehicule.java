package oop;

public class Vehicule {
 public String name;
 public int maxSpeed;
 
 public Vehicule() {}
 public Vehicule(String name) {
	 this.name = name;
 }
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getMaxSpeed() {
	return maxSpeed;
}
public void setMaxSpeed(int maxSpeed) {
	this.maxSpeed = maxSpeed;
}
 
 
 
}
